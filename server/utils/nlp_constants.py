_jieba_initialized = False
jieba = None
pseg = None


def _init_jieba():
    """
    延迟初始化 jieba（只在首次使用时初始化）
    
    Returns:
        tuple: (jieba, pseg) 模块引用
    """
    global jieba, pseg, _jieba_initialized
    if _jieba_initialized:
        return jieba, pseg
    
    try:
        import jieba as _jieba
        import jieba.posseg as _pseg
        jieba = _jieba
        pseg = _pseg
        if jieba is not None:
            for term in STOCK_TERMS:
                jieba.add_word(term, freq=1000, tag='n')
        _jieba_initialized = True
    except ImportError:
        jieba = None
        pseg = None
        _jieba_initialized = True
    
    return jieba, pseg


def get_jieba():
    """获取 jieba 模块（延迟初始化）"""
    _init_jieba()
    return jieba


def get_pseg():
    """获取 jieba.posseg 模块（延迟初始化）"""
    _init_jieba()
    return pseg
